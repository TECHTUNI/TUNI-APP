class Productdetails {
  final String id;
  final String name;
  final String brand;
  final String gender;
  final String price;
  final String time;
  final List<dynamic> imageUrlList;
  final String quantity;
  final String color;
  final List<dynamic> size;

  Productdetails({
    required this.id,
    required this.name,
    required this.brand,
    required this.gender,
    required this.price,
    required this.time,
    required this.imageUrlList,
    required this.quantity,
    required this.color,
    required this.size,
  });
}
